        <h3>Top Categories:</h3>
        <ul>
            <li><strong>1. How can I change/ cancel my reservation?</strong></li>
            <p>Visit the View/Change bookings option on the homepage of our website which has a list of features that allow you to retrieve and re-print your itineraries. You can also process changes/ cancellations to the itinerary service request additions (meals, seat, pre-paid excess baggage etc.) in your booking. You can also update your contact information on this page.</p>

            <li><strong>2. Till when can I cancel/ refund my booking online?</strong></li>
            <p>On domestic flights you can cancel/ refund till 72 hours prior to flight departure.</p>

            <li><strong>3. What is the procedure to cancel my flight?</strong></li>
            <p>The customers booking can be cancelled/ changed before the scheduled time of departure by contacting Airline India Call Centre, at Airline India's Airport Counters, on Airline India's website and through participating travel agent responsible for original booking
                <ul>
                    <li>72 hours prior to flight departure for domestic flights</li>
                    <li>4 hours prior to flight departure for international flights</li>
                </ul>
            </p>

            <li><strong>4. What is the age group of an infant?</strong></li>
            <p>For safety reasons, children above the age of 3 days and under the age of 2 years, as on the date of travel, can travel as Infants. Age proof needs to be provided at the time of check-in.</p>
            <p>Valid ID proof for Infants:
                <ul>
                    <li>Birth Certificate</li>
                    <li>Mother's hospital discharge summary</li>
                    <li>Vaccination certificate</li>
                    <li>Passport</li>
                </ul>
            </p>

            <li><strong>5. How many infants can travel per flight?</strong></li>
            <p>A maximum of 12 infants can travel per flight on an Airbus A320 and a maximum of 6 infants per flight on an ATR. However, only one infant per adult is allowed per flight.</p>

            <li><strong>6. What is a Travel Certificate?</strong></li>
            <p>Travel certificate is a proof of travel provided at a charge of INR 300 per person per sector inclusive of GST.</p>

            <li><strong>7. Are there any additional charges if I make my payment through the call centre?</strong></li>
            <p>A booking fee of INR 199 for Domestic and INR 398 for International + GST per passenger per sector (or equivalent) will be applicable for all bookings made from the call center and airport ticketing counter.</p>

            <li><strong>8. How safe is it to give my Credit card number on the IVR?</strong></li>
            <p>With the IVR system, your credit card number is punched through the keypad on your phone and not provided verbally to the Contact Centre agent. Your credit card details are encrypted and are masked in the PNR / reservation record and not accessible to the Customer Care agent. The entire process is fully automated and ensures security of your card information.</p>
